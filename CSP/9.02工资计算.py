T = int(input())
t = T - 3500  # 税后工资减去个税起征点3500，得到应纳税部分
# 逆向推算税前工资 S
if t <= 0:  # 如果 t<=0，说明税前工资未超过 3500，无需纳税
    A = t
elif t <= 1455:  # 适用于 3% 税率区间
    A = t / 0.97
elif t <= 4155:  # 适用于 10% 税率区间
    A = 1500 + (t - 1455) / 0.9
elif t <= 7755:  # 适用于 20% 税率区间
    A = 4500 + (t - 4155) / 0.8
elif t <= 27255:  # 适用于 25% 税率区间
    A = 9000 + (t - 7755) / 0.75
elif t <= 41255:  # 适用于 30% 税率区间
    A = 35000 + (t - 27255) / 0.7
elif t <= 57505:  # 适用于 35% 税率区间
    A = 55000 + (t - 41255) / 0.65
else:  # 适用于 45% 税率区间
    A = 80000 + (t - 57505) / 0.55
# 计算最终税前工资 S = A + 3500（起征点加回去）
print(int(A) + 3500)
